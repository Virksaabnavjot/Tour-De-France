    private void saveBasketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBasketActionPerformed
        // save objects to a file 
        //opening  a file pointer
        File outFile;
        FileOutputStream fStream;
        ObjectOutputStream oStream;
        //using try-catch followed by IoException method to solve and display errors in case if  file is corrupted or any related error
        try{
            outFile = new File("basketFile.data");
            fStream = new FileOutputStream(outFile);
            oStream = new ObjectOutputStream(fStream);
            oStream.writeObject(aList);
            JOptionPane.showMessageDialog(null,"Tour de France Store \n \nBasket was sucessfully saved. ");
            //closing the ObjectOutputStream at the end 
            oStream.close();
        }
        catch(IOException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_saveBasketActionPerformed

    private void viewSavedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSavedActionPerformed
        // TODO add your handling code here:
        File inFile;
        FileInputStream fStream;
        ObjectInputStream oStream;
        
        try{
            inFile = new File("basketFile.data");
            fStream = new FileInputStream(inFile);
            oStream = new ObjectInputStream(fStream);
            
            ArrayList <BasketObject> xList;
            //here readObject method reads objects from the file
            xList = (ArrayList<BasketObject>)oStream.readObject();
            for(BasketObject x:xList){
            JOptionPane.showMessageDialog(null, "Tour de France Store \n \n Size: "+x.getSize()+"\n Color: "+x.getColor()+"\n Quantity: "+x.getQuantity());
            }
            //closing the ObjectInputStream
            oStream.close();
        }
        catch(IOException e){
            System.out.println(e);
        }
        catch(ClassNotFoundException ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_viewSavedActionPerformed


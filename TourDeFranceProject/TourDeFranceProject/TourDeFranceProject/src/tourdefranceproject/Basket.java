    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        //to check if there is any object stored inside to delete
        if (sizeTf.getText().equals("") || colorTf.getText().equals("") || quantityTf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Sorry, you need to enter Size, Color and quantity to delete an object");
        } //to check if user had submitted anything yet
        else if (count == 0) {
            JOptionPane.showMessageDialog(null, "Sorry, you have not placed any Order yet");
        } // to delete an object from an array
        else {
            size = sizeTf.getText();
            color = colorTf.getText();
            quantity = Integer.parseInt(quantityTf.getText());

            for (int i = 0; i < aList.size(); i++) {
                if (aList.get(i).getSize().equalsIgnoreCase(size) && aList.get(i).getColor().equalsIgnoreCase(color) && aList.get(i).getQuantity() == quantity) {
                    aList.remove(i);
                    count = count - 1;

                    JOptionPane.showMessageDialog(null, "Item Deleted \n Size: " + size + "\n Color: " + color + "\n Quantity " + quantity + " \n Now, there is Only " + count + "  items left in Basket");
                    break;
                } else if (i == count - 1) {
                    JOptionPane.showMessageDialog(null, "This item does not exists");
                }
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

